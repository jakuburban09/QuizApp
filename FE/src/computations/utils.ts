export function getDataFromString(
  separatorOne: string = firstSeparator,
  separatorTwo: string = secondSeparator,
  inputString: string = inputData,
): {
  foreignWord: string;
  translation: string;
  explanation: string;
  ID: number;
}[] {
  const regex = new RegExp(`${separatorOne}|${separatorTwo}|\n`, "g");

  const resultArray: string[] = inputString
    .split(regex)
    .filter((item) => item.trim() !== "");
  console.log(resultArray);

  const arrayOfObjects: {
    foreignWord: string;
    translation: string;
    explanation: string;
    ID: number;
  }[] = [];

  resultArray.forEach((item, index) => {
    if (index % 3 === 0) {
      const dataObject = {
        foreignWord: item.trim(),
        translation: resultArray[index + 1]?.trim() || "",
        explanation: resultArray[index + 2]?.trim() || "",
        ID: index / 3 + 1,
      };
      arrayOfObjects.push(dataObject);
    }
  });

  return arrayOfObjects;
}

export function getRandomItemsFromArray<T>(
  array: T[],
  numberOfItems: number,
): T[] {
  const shuffledArray = array.sort(() => Math.random() - 0.5);
  return shuffledArray.slice(0, numberOfItems);
}

export function createQuizQuestions(
  allItems: {
    foreignWord: string;
    translation: string;
    explanation: string;
    ID: number;
  }[],
  randomItems: {
    foreignWord: string;
    translation: string;
    explanation: string;
    ID: number;
  }[],
): {
  question: string;
  options: string[];
  correctOption: string;
  index: number;
}[] {
  const quizQuestions: {
    question: string;
    options: string[];
    correctOption: string;
    index: number; // Přidáme index
  }[] = [];

  randomItems.forEach((randomItem, index) => {
    // Přidáme index
    const correctOption = randomItem.translation;
    const otherTranslations = allItems
      .filter((item) => item.ID !== randomItem.ID)
      .map((item) => item.translation);

    const randomTranslations = getRandomItemsFromArray(otherTranslations, 3);

    const options = [...randomTranslations, correctOption].sort(
      () => Math.random() - 0.5,
    );

    quizQuestions.push({
      question: randomItem.foreignWord,
      options: options,
      correctOption: correctOption,
      index: index, // Přidáme index
    });
  });

  return quizQuestions;
}

/**
 * Enum for console message types.
 * @readonly
 * @enum {string}
 */
export const LogType = {
  LOG: "log",
  INFO: "info",
  WARN: "warn",
  ERROR: "error",
  TABLE: "table",
} as const;

/**
 * Displays a message in the console with the specified log type.
 *
 * @template T - The type of the message to be displayed.
 *
 * @param {T | any[]} message - The message or data to be displayed.
 * @param {typeof LogType} [logType=LogType.LOG] - The type of console log to use.
 *
 * @example
 * // Display a regular log message
 * log("Hello, World!"); // Equivalent to console.log("Hello, World!");
 *
 * // Display an informational message
 * log("Informational message", LogType.INFO); // Equivalent to console.info("Informational message");
 *
 * // Display a warning message
 * log("Warning message", LogType.WARN); // Equivalent to console.warn("Warning message");
 *
 * // Display an error message
 * log("Error message", LogType.ERROR); // Equivalent to console.error("Error message");
 *
 * // Display a table in the console
 * const data = [{ name: 'John', age: 30 }, { name: 'Jane', age: 25 }];
 * log(data, LogType.TABLE); // Equivalent to console.table(data);
 */
export function log<T>(
  message: T | any[],
  logType: (typeof LogType)[keyof typeof LogType] = LogType.LOG,
) {
  switch (logType) {
    case LogType.LOG:
      return (() => {
        console.log(message);
        return "";
      })();

    case LogType.INFO:
      return (() => {
        console.info(message);
        return "";
      })();

    case LogType.WARN:
      return (() => {
        console.warn(message);
        return "";
      })();

    case LogType.ERROR:
      return (() => {
        console.error(message);
        return "";
      })();

    case LogType.TABLE:
      return (() => {
        console.table(message);
        return "";
      })();

    default:
      console.log(message);
  }
}
